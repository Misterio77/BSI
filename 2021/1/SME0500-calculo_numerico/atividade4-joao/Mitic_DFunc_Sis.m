function [dy] = DFunc_Sis(x)
     dy(1,1) = 4 - x(4);
     dy(1,2) = -1;
     dy(1,3) = 1;
     dy(1,4) = -x(1);
     dy(2,1) = -1;
     dy(2,2) = 3 - x(4);
     dy(2,3) = -2;
     dy(2,4) = -x(2);
     dy(3,1) = 1;
     dy(3,2) = -2;
     dy(3,3) = 3 - x(4);
     dy(3,4) = -x(3);
     dy(4,1) = 2*x(1);
     dy(4,2) = 2*x(2);
     dy(4,3) = 2*x(3);
     dy(4,4) = 0;
     return
end
